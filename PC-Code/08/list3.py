# Updating a list 
#   1. insert method
#   2. append method

# 1. Insert Method

a = [1,2,3,4,5]
# a.insert(index,element to insert)
a.insert(1,"alpha")
# all other element will shift to right side 
print(a)

# 2. Append Method
b = [1,2,3,4,5]
#  add the element at last of list 
b.append('ranjeet')
print(b)