# Adding 
s1={1,2,3,4}
s1.add(5)
print(s1)

# Update
s1.update([5,6])
print(s1)


# Delete 
# del
s2={1,2,3,4,5}
del s2
# discard
s3={1,2,3,4,5}
s3.discard(5)
print(s3)
# remove
s3.remove(2)
print(s3)
# pop

# pop -> randomly delete any element 
s3.pop()

# clear
# empty set
s3.clear() 